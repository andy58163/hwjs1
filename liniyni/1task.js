// ПЕРША ЗАДАЧА Обчислити площу та периметр прямокутника, довжини сторін якого задаються.
let rectSideA = parseFloat(prompt('Сторона прямокутника №=1'));
let rectSideB = parseFloat(prompt('Сторона прямокутника №=2'));
let pRectangle = 2 * rectSideA + 2 * rectSideB;
document.write(`Периметр прямокутника = ${pRectangle}`);
